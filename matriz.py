"""
Descripción del Código:
Este código imprime las filas de una lista bidimensional en Python, proporcionando una descripción general del propósito del código.
"""

# Definición de la lista bidimensional 'lista'
lista = [[2, -3, 1], [-4, 2, 0], [5, 7, 1]]

# Iteración sobre las filas de la lista e impresión
for row in lista:
    print(row)
